public class Primitivedt{
    public static void main(String[] args) {
        byte a=-128;
        byte b=127;
        System.out.println("The range of byte is : "+a+","+b);

        short sh1=-29999;
        short sh2=32767;
        System.out.println("The range of short is"+sh1+","+sh2);

        long l=250L;
        System.out.println(l);

        boolean b1=true;
        boolean b2=false;
        System.out.println("the values of boolean is : "+b1+","+b2);

        float f=10.5f;
        System.out.println(f);
        double d=200000.25999;
        System.out.println(d);
    }
}